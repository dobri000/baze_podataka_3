    public void initTable() {
        try {
            List<Angazovanje> angazovanja = DBBAngazovanje.vratiSvaAngazovanja(radniDnevnik);
            AngazovanjeTableModel model = new AngazovanjeTableModel(angazovanja);
            table.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
