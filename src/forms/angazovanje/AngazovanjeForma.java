    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = table.getSelectedRow();
        if(row == -1)
            return;
        AngazovanjeTableModel model = (AngazovanjeTableModel) table.getModel();
        Angazovanje angazovanje = model.getAngazovanje(row);
        angazovanje.setSifraRadnogDnevnika(radniDnevnik.getSifraRadnogDnevnika());
        new AzurirajAngazovanjeForma(this, true, angazovanje).setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int row = table.getSelectedRow();
        if(row == -1)
            return;
        AngazovanjeTableModel model = (AngazovanjeTableModel) table.getModel();
        Angazovanje angazovanje = model.getAngazovanje(row);
        angazovanje.setSifraRadnogDnevnika(radniDnevnik.getSifraRadnogDnevnika());
        try {
            DBBAngazovanje.obrisiAngazovanje(angazovanje);
            initTable();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

