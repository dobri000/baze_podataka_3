    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int danUMesecu = Integer.parseInt(txtDanUMesecu.getText());
        int sifraObjekta = Integer.parseInt(txtSifraObjekta.getText());
        int standardniSati = Integer.parseInt(txtStandardniSati.getText());
        int prekovremeniSati = Integer.parseInt(txtPrekovremeniSati.getText());
        RadniDan radniDan = new RadniDan(danUMesecu, angazovanje.getSifraRadnogDnevnika());
        try {
            if(DBBRadniDan.daLiPostojiRadniDan(radniDan) == false) {
                DBBRadniDan.dodajRadniDan(radniDan);
            }
            RadniSati radniSati = new RadniSati(standardniSati, prekovremeniSati);
            Angazovanje novoAngazovanje = new Angazovanje(danUMesecu, sifraObjekta, radniSati);
            DBBAngazovanje.azurirajAngazovanje(angazovanje, novoAngazovanje);
            AngazovanjeForma forma = (AngazovanjeForma) getParent();
            forma.initTable();
            this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

