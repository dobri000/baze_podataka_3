    public void initTable() {
        try {
            List<Ponuda> ponude = DBBPonuda.vratiSvePonude();
            PonudaTableModel model = new PonudaTableModel(ponude);
            txtPonuda.setModel(model);
        } catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
        
    }
