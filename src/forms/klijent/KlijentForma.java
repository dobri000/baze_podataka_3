    public void initTable() {
        try {
            List<Klijent> klijenti = DBBKlijent.vratiSveKlijente();
            KlijentTableModel model = new KlijentTableModel(klijenti);
            tableKlijent.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
