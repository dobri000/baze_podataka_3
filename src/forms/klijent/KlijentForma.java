    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = tableKlijent.getSelectedRow();
        if(row == -1) {
            return;
        }
        KlijentTableModel model = (KlijentTableModel) tableKlijent.getModel();
        Klijent klijent = model.getKlijent(row);
        new AzurirajKlijentaForma(this, true, klijent).setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = tableKlijent.getSelectedRow();
        if(row == -1) {
            return;
        }
        KlijentTableModel model = (KlijentTableModel) tableKlijent.getModel();
        Klijent klijent = model.getKlijent(row);
        try {
            DBBKlijent.obrisiKlijenta(klijent);
            initTable();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    

