    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int rbStavke = Integer.parseInt(txtRBStavke.getText());
        int oznaka = Integer.parseInt(txtOznaka.getText());
        double kolicina = Double.parseDouble(txtKolicina.getText());
        JedinicaMere nazivJM = (JedinicaMere) comboJM.getSelectedItem();
        StavkaOtpremnice stavka = new StavkaOtpremnice(otpremnica.getBrojOtpremnice(), rbStavke, oznaka, kolicina, nazivJM);
        try {
            DBBStavkaOtpremnice.dodajStavkuOtpremnice(stavka);
            OtpremnicaForma forma = (OtpremnicaForma) getParent();
            forma.reloadTableStavke(otpremnica);
            dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

