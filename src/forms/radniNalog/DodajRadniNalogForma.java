    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int brojRadnogNaloga = Integer.parseInt(txtBrojRadnogNaloga.getText());
        LocalDate rokZaRealizaciju = LocalDate.parse(txtRokZaRealizaciju.getText(), DateTimeFormatter.ofPattern("yyyy-MM-dd"));
        String opisSistema = txtOpisSistema.getText();
        String obavljenRad = txtObavljenRad.getText();
        LocalDate datumObavljanjaKontrole = LocalDate.parse(txtDatumObavljanjaKontrole.getText(), DateTimeFormatter.ofPattern("yyyy-MM-dd"));
        int sifraKontakta = Integer.parseInt(txtSifraKontakta.getText());
        String nazivGrada = txtGrad.getText();
        LocalDate datumOtvaranjaRadnogNaloga = LocalDate.parse(txtDatumOtvaranjaRN.getText(), DateTimeFormatter.ofPattern("yyyy-MM-dd"));
        int brPonude = Integer.parseInt(txtBrPonude.getText());
        int sifraZaposlenog = Integer.parseInt(txtSifraZaposlenog.getText());
        try {
            int sifraGrada = DBBGrad.nadjiGradPoImenu(nazivGrada);
            RadniNalog nalog = new RadniNalog(brojRadnogNaloga, rokZaRealizaciju, opisSistema, obavljenRad, datumOtvaranjaRadnogNaloga, datumObavljanjaKontrole, brPonude, sifraKontakta, sifraZaposlenog, sifraGrada);
            DBBRadniNalog.dodajRadniNalog(nalog);
            RadniNalogForma forma = (RadniNalogForma) getParent();
            forma.initTables();
            this.dispose();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

